    private void btnCargarCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarCSVActionPerformed
  {                                             
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Seleccionar archivo de usuarios CSV");
        
        javax.swing.filechooser.FileNameExtensionFilter filtro = new javax.swing.filechooser.FileNameExtensionFilter("Archivos CSV", "csv");
        fileChooser.setFileFilter(filtro);
        
        int seleccion = fileChooser.showOpenDialog(this);
        
        if (seleccion == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File archivo = fileChooser.getSelectedFile();
            String ruta = archivo.getAbsolutePath();
            
            boolean exito = simulador.cargarUsuariosDesdeCSV(ruta);
            
            if (exito) {
                javax.swing.JOptionPane.showMessageDialog(this, "Usuarios cargados correctamente.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                actualizarVistas(); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar el archivo.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_btnCargarCSVActionPerformed

