        /**
     * Método auxiliar para refrescar la interfaz gráfica con los datos actuales.
     */
    private void actualizarVistas() {
        // 1. Actualizar el JTextArea de Usuarios (txtAreaUsuarios)
        StringBuilder sbUsuarios = new StringBuilder();
        sbUsuarios.append("--- USUARIOS REGISTRADOS ---\n");
        for (int i = 0; i < simulador.getCantidadUsuarios(); i++) {
            sbUsuarios.append(simulador.getUsuarios()[i].toString()).append("\n");
        }
        txtAreaUsuarios.setText(sbUsuarios.toString());

        // 2. Actualizar el JTextArea de la Cola de Impresión (txtAreaCola)
        StringBuilder sbCola = new StringBuilder();
        sbCola.append("--- COLA DE IMPRESIÓN (Montículo) ---\n");
        modelos.RegistroImpresion[] arreglo = simulador.getColaImpresion().getArreglo();
        for (int i = 0; i < simulador.getColaImpresion().getTamanoActual(); i++) {
            sbCola.append(arreglo[i].toString()).append("\n");
        }
        txtAreaCola.setText(sbCola.toString());
        dibujadorArbol.repaint();
        
    }

