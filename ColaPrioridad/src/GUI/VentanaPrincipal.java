    private void btnAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUsuarioActionPerformed
String idUsuario = txtIdUsuario.getText().trim();
        String tipoUsuario = cbxTipoUsuario.getSelectedItem().toString();

        if (idUsuario.isEmpty()) {
            // Cumpliendo rúbrica: Alerta de error
            javax.swing.JOptionPane.showMessageDialog(this, "El ID de usuario no puede estar vacío.", "Error de validación", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (simulador.buscarUsuario(idUsuario) != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "El usuario ya existe en el sistema.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        simulador.agregarUsuario(idUsuario, tipoUsuario);
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario agregado con éxito.");
        txtIdUsuario.setText(""); // Limpiar campo
        actualizarVistas();
    }//GEN-LAST:event_btnAgregarUsuarioActionPerformed

    private void btnEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUsuarioActionPerformed
String idUsuario = txtIdUsuario.getText().trim();

        if (idUsuario.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el ID del usuario a eliminar.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean eliminado = simulador.eliminarUsuario(idUsuario);
        if (eliminado) {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");
            txtIdUsuario.setText("");
            actualizarVistas();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontró un usuario con ese ID.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarUsuarioActionPerformed

    private void btnEnviarImpresionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarImpresionActionPerformed
String idUsuario = txtIdUsuario.getText().trim();
        String nombreDoc = txtNombreDoc.getText().trim();
        String tamanoStr = txtTamanoDoc.getText().trim();
        // Nota: Asume que agregaste un campo txtTipoDoc, si no lo tienes, pon un texto fijo como "PDF"
        String tipoDoc = txtTipoDoc.getText().trim(); 
        boolean esPrioritario = chkPrioridad.isSelected();

        // Validaciones de seguridad (Rúbrica de Usabilidad)
        if (idUsuario.isEmpty() || nombreDoc.isEmpty() || tamanoStr.isEmpty() || tipoDoc.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor llene todos los campos del documento y el ID del usuario.", "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int tamano = Integer.parseInt(tamanoStr);
            if (tamano <= 0) throw new NumberFormatException();

            modelos.Usuario u = simulador.buscarUsuario(idUsuario);
            if (u != null) {
                // Creamos el documento, lo agregamos al usuario y lo mandamos a la cola
                modelos.Documento doc = new modelos.Documento(nombreDoc, tamano, tipoDoc, esPrioritario);
                u.agregarDocumento(doc); 
                simulador.enviarAImpresion(idUsuario, doc);
                
                simulador.tickReloj(); // El tiempo del sistema avanza
                
                javax.swing.JOptionPane.showMessageDialog(this, "Documento encolado correctamente.");
                
                // Limpiar campos
                txtNombreDoc.setText("");
                txtTamanoDoc.setText("");
                txtTipoDoc.setText("");
                chkPrioridad.setSelected(false);
                
                actualizarVistas();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "El usuario ingresado no existe. Regístrelo primero.", "Usuario no encontrado", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "El tamaño del documento debe ser un número entero mayor a 0.", "Dato inválido", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEnviarImpresionActionPerformed

    private void btnCancelarImpresionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarImpresionActionPerformed
String idUsuario = txtIdUsuario.getText().trim();
        String nombreDoc = txtNombreDoc.getText().trim();

        if (idUsuario.isEmpty() || nombreDoc.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Para cancelar, ingrese el ID del usuario y el Nombre del documento.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean cancelado = simulador.cancelarImpresion(idUsuario, nombreDoc);
        if (cancelado) {
            javax.swing.JOptionPane.showMessageDialog(this, "El documento fue cancelado y retirado de la cola exitosamente.");
            simulador.tickReloj(); // El tiempo avanza
            actualizarVistas();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontró el documento en la cola. Asegúrese de que el nombre y el usuario sean correctos.", "Error al cancelar", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCancelarImpresionActionPerformed

    private void btnLiberarImpresoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberarImpresoraActionPerformed
modelos.RegistroImpresion impreso = simulador.liberarImpresora();
        
        if (impreso != null) {
            String msj = "Se ha impreso exitosamente:\n" + 
                         "Documento: " + impreso.getDocumento().getNombre() + "." + impreso.getDocumento().getTipo() + "\n" +
                         "Tamaño: " + impreso.getDocumento().getTamano() + " páginas.";
            
            javax.swing.JOptionPane.showMessageDialog(this, msj, "Impresión Completada", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            simulador.tickReloj(); // El tiempo avanza por imprimir
            actualizarVistas();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "La cola de impresión está vacía. No hay nada que imprimir.", "Aviso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnLiberarImpresoraActionPerformed

